export const searchFAQPure = (faqObj, query) => {
  const raw = String(query || "").toLowerCase().trim();
  if (!raw) return [];

  // ë¬¸ì¥í˜• ì…ë ¥ì—ì„œ êµ¬ë‘ì  ì œê±° + í† í° ë¶„ë¦¬
  const cleaned = raw.replace(/[?.,!~]/g, " ");
  const parts = cleaned.split(/\s+/).filter(Boolean);

  // í‚¤ì›Œë“œë§Œ ì¶”ì¶œ (ì¡°ì‚¬/ì–´ë¯¸, 1ê¸€ì í† í° ì œê±°) + ë™ì˜ì–´ ë§¤í•‘
  const tokens = parts
    .filter((t) => t.length >= 2 && !STOPWORDS.has(t))
    .map((t) => SYNONYMS[t] || t);

  const results = [];

  for (let id in faqObj) {
    const { q, a } = faqObj[id];
    const ql = q.toLowerCase();
    const al = a.toLowerCase();

    let score = 0;

    if (tokens.length > 0) {
      // ì—¬ëŸ¬ í‚¤ì›Œë“œ ê¸°ë°˜ ìŠ¤ì½”ì–´ë§
      tokens.forEach((tok) => {
        if (ql.includes(tok)) {
          score += ql.startsWith(tok) ? 6 : 4;
        }
        if (al.includes(tok)) {
          score += 2;
        }
      });
    } else {
      // í‚¤ì›Œë“œ ë¶„ë¦¬ê°€ ì•ˆ ë  ê²½ìš°, ì›ë˜ ë°©ì‹ìœ¼ë¡œ fallback
      score =
        (ql.startsWith(raw) ? 3 : 0) +
        (ql.includes(raw) ? 2 : 0) +
        (al.includes(raw) ? 1 : 0);
    }

    if (score > 0) {
      results.push({ id: Number(id), q, a, score });
    }
  }

  return results.sort((a, b) => b.score - a.score || a.id - b.id);
};

